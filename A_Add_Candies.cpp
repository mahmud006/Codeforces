#include <bits/stdc++.h>
using namespace std;
#define ll long long
int main()
{
    ll t;
    cin >> t;
    while (t--)
    {
        ll n;
        cin >> n;
        cout << n << endl;
        for (ll i = 0; i < n; i++)
        {
            cout << i + 1 << " ";
        }
        cout << endl;
    }
}